public class Dog extends RescueAnimal {

    // Instance variable
    private String breed;

    // Constructor
    public Dog() {
    }

    // Accessor Method
    public String getBreed() {
        return breed;
    }

    // Mutator Method
    public void setBreed(String dogBreed) {
        breed = dogBreed;
    }

}
